int	ft_sqrt(int nb)
{
	long int	i;

	i = 1;
	if (nb == 0 || nb == 1)
		return (nb);
	while (i * i <= nb)
	{
		if (nb == i * i)
			return (i);
		i++;
	}
	return (0);
}
