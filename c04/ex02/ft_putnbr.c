#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nbr)
{
	long	x;

	x = nbr;
	if (x < 0)
	{
		ft_putchar('-');
		x = -x;
	}
	if (x >= 10)
	{
		ft_putnbr(x / 10);
		ft_putchar(x % 10 + '0');
	}
	if (x < 10)
	{
		ft_putchar(x % 10 + '0');
	}
}
